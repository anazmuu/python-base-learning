from math import *

number_a = 6
number_b = 10
number_c = -5

print(number_a + number_b) #operasi pertambahan
print(number_a - number_b) #operasi pengurangan
print(number_a * number_b) #operasi perkalian
print(number_a / number_b) #operasi pembagian
print(number_a % number_b) #operasi modulus
print(str(number_a) + " adalah angka 6") #int di str kan terlebih dahulu
print(abs(number_c)) #abs merubah negatif menjadi positif
print(pow(5, 3)) #pow adalah pemangkatan 5 * 5 * 5
print(max(12, 15, 4, 20)) #mencari nilai maksimum
print(min(12, 15, 4, 20)) #mencari nilai minimum
print(round(3.6)) #mencari hasil nilai bulat
print(floor(3.7))
print(ceil(3.6))
print(sqrt(64)) #pembulatan
